    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Botão Cadastrar trabalhador:
        String nome = jTextField1.getText();
        float salario = Float.parseFloat(jTextField2.getText());
        char sexo = jTextField3.getText().toUpperCase().charAt(0);
        
        //Criamos um objeto da classe Trabalhador:
        Trabalhador trabalh = new Trabalhador(nome,salario,sexo);
        
        //Acessamos o atributo estático 'empresa' da JanelaPrincipal,
        //executamos o método adicionarTrabalhador da classe Empresa, 
        //para adicionar este novo trabalhador 'trabalh' no cadastro 
        //(no ArrayList trab da classe Empresa): 
        Empresa.empresa.adicionarTrabalhador(trabalh);
        
        JOptionPane.showMessageDialog(null, "Trabalhador cadastrado satisfatoriamente",
                    "Trabalhador", JOptionPane.INFORMATION_MESSAGE);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

