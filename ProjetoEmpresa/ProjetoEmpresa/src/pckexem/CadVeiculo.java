    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String placa = jTextField1.getText();
        String marca = jTextField2.getText();
        String modelo = jTextField3.getText();
        int ano = Integer.parseInt(jTextField4.getText());
        float km = Float.parseFloat(jTextField6.getText());
        float preco = Float.parseFloat(jTextField5.getText());
        
        Veiculo veic = new Veiculo(placa, marca, modelo, ano,km, preco);
        
        Empresa.empresa.adicionarVeiculo(veic);
        
        JOptionPane.showMessageDialog(null, "Veiculo cadastrado satisfatoriamente",
                    "Trabalhador", JOptionPane.INFORMATION_MESSAGE);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

