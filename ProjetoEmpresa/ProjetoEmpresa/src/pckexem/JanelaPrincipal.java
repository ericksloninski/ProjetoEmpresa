    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        // Opção de menu Média dos salários da Empresa:
        if (Empresa.empresa.getQtdeTrabalhadores() > 0) {
            float media = Empresa.empresa.mediaSalariosEmpresa();
            JOptionPane.showMessageDialog(null,
                    "Média dos salários dos trabalhadores da Empresa: R$ "
                    + media, "Média dos salários", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Não existem trabalhadores cadastrados!",
                    "Média dos salários", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // Opção de menu Mostra trabalhadores cadastrados:
        if (Empresa.empresa.getQtdeTrabalhadores() > 0) {
            String txt = Empresa.empresa.getDadosTrabalhadores();
            JOptionPane.showMessageDialog(null, "Lista dos trabalhadores da Empresa \n\n"
                    + txt, "Trabalhadores da empresa", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Não existem trabalhadores cadastrados!",
                    "Trabalhadores", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

